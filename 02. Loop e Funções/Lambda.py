# Funcao completa

def potencia(num):
    resultado = num ** 2
    return resultado

potencia(5)


# Funcao menor

def potencia(num):
    return num ** 2

potencia(5)


# Funcao em uma linha

def potencia(num): return num ** 2

potencia(5)


# Expressao lamba - funcao anonima

potencia = lambda num: num ** 2

potencia(5)

